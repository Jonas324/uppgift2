public class Animal {

    private int weight;
    private boolean hungry;

    public Animal(int weight, boolean hungry) {
        this.weight = weight;
        this.hungry = hungry;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public boolean isHungry() {
        return hungry;
    }

    public void setHungry(boolean hungry) {
        this.hungry = hungry;
    }




}
